package com.ivygames.morskoiboi.screen.view;

public class Mark {
    public int centerX;
    public int centerY;
    public float outerRadius;
    public float innerRadius;

    @Override
    public String toString() {
        return "Mark{" +
                "centerX=" + centerX +
                ", centerY=" + centerY +
                ", outerRadius=" + outerRadius +
                ", innerRadius=" + innerRadius +
                '}';
    }
}
